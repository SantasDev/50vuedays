<template>
  <div class="container">
    <p class="joke"> {{ joke }}</p>
    <button class="btn" @click="anotherJoke"> Get another joke</button>
  </div>
</template>

<script setup lang="ts">
import { ref } from 'vue';

const joke = ref('');

const anotherJoke = async () => {
  const config = {
    headers: {
      Accept: 'application/json',
    },
  }

  const res = await fetch('https://icanhazdadjoke.com', config)

  const data = await res.json()

  joke.value = data.joke
}

</script>

